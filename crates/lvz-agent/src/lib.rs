/// Tunable settings for an agent run that are *not* efficiency knobs (those are [`Knobs`],
/// chosen per task by a [`Tuner`]).
#[derive(Clone, Debug)]
pub struct AgentConfig {
    /// Model id passed to the provider each turn.
    pub model: String,
    /// Per-turn generation ceiling.
    pub max_tokens: u32,
    /// System prompt (the stable prefix; cached when the provider supports it).
    pub system: String,
    /// Safety cap on tool round-trips before forcing a stop.
    pub max_steps: usize,
    /// Optional ceiling on total task tokens across all round-trips (§6.4).
    pub token_budget: Option<u64>,
    /// Coarse tier of [`model`](Self::model); conditions tuner knob selection (§6.6).
    pub model_tier: ModelTier,
    /// Cheaper model id used for history-compaction summaries (§6.3 model routing). When
    /// `None`, summaries reuse [`model`](Self::model).
    pub summary_model: Option<String>,
    /// Soft per-request ceiling on estimated context tokens (§6.3 context-budget manager).
    /// When set and exceeded even after compaction, the oldest tool-result content is evicted.
    /// Distinct from [`token_budget`](Self::token_budget), which caps the *whole task*.
    pub context_limit: Option<usize>,
    /// Repository root to profile for the tuner's [`TaskContext`] (§6.6). `None` skips the
    /// filesystem walk and uses an empty [`RepoProfile`] — fine for the [`NoopTuner`] default.
    pub repo_root: Option<PathBuf>,
    /// Optional cheaper model used for the *first* turns; the loop escalates to
    /// [`model`](Self::model) after [`escalate_after`](Self::escalate_after) round-trips
    /// (cheap-model-first cost reduction, §8). `None` runs every turn on [`model`](Self::model).
    pub cheap_model: Option<String>,
    /// Round-trips to run on [`cheap_model`](Self::cheap_model) before escalating to the strong
    /// model. Ignored when `cheap_model` is `None`.
    pub escalate_after: usize,
    /// Optional **smarter, more expensive** "advisor" model that drafts a plan in one tool-less
    /// call before the loop (advisor+executor split, §8). The plan seeds the **cheaper**
    /// executor — the main [`model`](Self::model) that runs the loop — so the expensive model is
    /// paid for *once* (a short planning call) while the cheap model does the many execution
    /// turns. `None` disables the pre-pass. E.g. advisor = Opus, executor `model` = Sonnet.
    pub advisor_model: Option<String>,
    /// Optional shell command run (in [`repo_root`](Self::repo_root)) after a task completes
    /// normally; exit 0 means the change is good. This is the real ATO **success signal**
    /// (`RECIPE.md` §6.6, `docs/ATO.md`) — e.g. `"cargo test --quiet"`. When `None`, success
    /// falls back to the coarse "completed without error" signal. Only run on otherwise-clean
    /// completion (not after a provider error / budget overrun / step-cap).
    pub verify_command: Option<String>,
    /// Classify the task [`Archetype`] with a **model call** instead of the free keyword
    /// heuristic (`RECIPE.md` §6.3/§6.5). Off by default — it costs one extra tool-less round-trip
    /// (routed to [`summary_model`](Self::summary_model) when set, else [`model`](Self::model);
    /// its tokens count toward the task total). On a parse failure it falls back to the heuristic.
    /// Only worth enabling with a learning tuner, where a sharper archetype key helps.
    pub classify_with_model: bool,
    /// Enable the **trace-based skeleton-radius counterfactual** (`docs/ATO.md` §6, §10).
    /// **Off by default and unsound** — unlike the always-on truncate counterfactual (which is
    /// exact), this *estimates* the tokens a smaller [`skeleton_radius`](Knobs::skeleton_radius)
    /// would have cost (by re-extracting the captured skeletons at each smaller radius) and
    /// **optimistically transfers the realised success bit** to those cheaper radii — it cannot
    /// prove the model wouldn't have failed with less context. It lets ATO discover smaller radii
    /// faster, at the risk of crediting a starved setting. Only meaningful with a learning tuner.
    pub radius_counterfactual: bool,
    /// Inject a **cache-aware repo-skeleton prefix** (`RECIPE.md` §6.1): when `Some(budget)`, a
    /// token-bounded skeleton of the whole repo (tree-sitter outlines of every source file under
    /// [`repo_root`](Self::repo_root), to `budget` estimated tokens) is built **once** and placed
    /// as the first content block of the first user message, marked cacheable. Ordered
    /// immutable→stable→volatile, it joins the cached prefix (system + tool defs + skeleton), so on
    /// a caching provider it is paid for once and re-read cheaply on every subsequent round-trip
    /// and task in the same repo — giving the model whole-repo structure without per-task reads.
    /// `None` disables it. Most valuable with prompt caching (Anthropic) and a long-running
    /// `--serve`; on a non-caching provider it still adds the skeleton but without the amortisation.
    pub repo_skeleton: Option<usize>,
    /// **In-loop verify** (convergence lever): run [`verify_command`](Self::verify_command) *during*
    /// the loop — after any turn that edited a file — and stop with success the moment it passes,
    /// instead of waiting for the model to decide it is done. Off by default; requires
    /// `verify_command`. Most reliable with a real test gate — with a weak lint proxy it can stop on
    /// an incomplete change that happens to lint clean.
    pub in_loop_verify: bool,
    /// **No-progress circuit-breaker** (convergence lever): when `Some(n)`, after `n` consecutive
    /// tool-call turns that edited nothing, inject a one-time nudge to act; after `2n`, hard-stop the
    /// loop (`Done(Other("no_progress"))`). Breaks the explore-forever paralysis. `None` disables it.
    pub no_progress_limit: Option<usize>,
    /// **Budget awareness** (convergence lever): append a short `[progress: turn x/max …]` note to
    /// each tool-result message so the model knows how close it is to the step/token ceiling and can
    /// wrap up deliberately rather than being truncated. Off by default. Lives in the volatile tail,
    /// so it does not disturb the cached prefix.
    pub budget_awareness: bool,
}

