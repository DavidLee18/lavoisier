//! `lvz-agent` — the reasoning loop (`RECIPE.md` §4, §6.3).
//!
//! The agent runs a **plan → act → observe** cycle: ask the provider for a turn, and if the
//! model called tools, execute them, append the results to history, and ask again — until
//! the model answers without calling a tool (or a safety/budget limit trips). It consumes
//! only the [`Provider`] and [`Tool`] contracts plus a [`ToolRegistry`], so it is unaware of
//! any wire protocol or gateway.
//!
//! Efficiency hardening (§6.3) lives here:
//! - **Caching** is capability-gated (§6.2): the stable prefix (system prompt + tool
//!   definitions) is marked cacheable only when the provider advertises
//!   [`prompt_caching`](lvz_protocol::Capabilities::prompt_caching).
//! - **History compaction** summarises old turns once the transcript exceeds the
//!   [`compact_after`](Knobs::compact_after) knob, keeping the original task and the most
//!   recent turns verbatim. The summary is generated by a separate, tool-less provider call
//!   that can be **routed to a cheaper model** ([`AgentConfig::summary_model`]) — model
//!   tiering for the routing/summary workload.
//! - **Tool-result truncation** caps oversized outputs head/tail.
//! - The efficiency dials are [`Knobs`] chosen by a [`Tuner`] (default [`NoopTuner`] = the
//!   static §6.5 baseline), and the realised [`Outcome`] is reported back so an ATO learner
//!   (`lvz-tune`, §6.6) can be swapped in without touching the agent.
//!
//! Token usage is summed across every round-trip — including the compaction call — which is
//! the metric that matters (§6.4) and is enforced against an optional budget.

