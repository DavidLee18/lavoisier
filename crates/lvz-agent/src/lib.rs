    /// Records every request's `(model, has_tools, system)` and routes the reply: a tool-less
    /// request (the compaction summary call) gets a short summary; a tool-bearing request pops
    /// the next scripted main-loop turn.
    struct RecordingProvider {
        seen: Mutex<Vec<(String, bool, String)>>,
        main_turn: AtomicUsize,
        main_scripts: Vec<Vec<Event>>,
    }

    impl RecordingProvider {
        fn new(main_scripts: Vec<Vec<Event>>) -> Arc<Self> {
            Arc::new(Self {
                seen: Mutex::new(Vec::new()),
                main_turn: AtomicUsize::new(0),
                main_scripts,
            })
        }
    }

    #[async_trait]
    impl Provider for RecordingProvider {
        async fn stream(
            &self,
            req: ChatRequest,
        ) -> Result<
            BoxStream<'static, Result<Event, lvz_protocol::ProviderError>>,
            lvz_protocol::ProviderError,
        > {
            let has_tools = !req.tools.is_empty();
            let system = req
                .system
                .as_ref()
                .map(|s| s.text.clone())
                .unwrap_or_default();
            self.seen
                .lock()
                .unwrap()
                .push((req.model.clone(), has_tools, system));

            let events = if has_tools {
                let n = self.main_turn.fetch_add(1, Ordering::SeqCst);
                self.main_scripts.get(n).cloned().unwrap_or_default()
            } else {
                // Compaction summary call.
                vec![
                    Event::TextDelta("- summarised earlier work".into()),
                    Event::Usage(Usage {
                        input_tokens: 30,
                        output_tokens: 8,
                        ..Default::default()
                    }),
                    Event::Done(StopReason::EndTurn),
                ]
            };
            Ok(stream::iter(events.into_iter().map(Ok)).boxed())
        }

        fn capabilities(&self) -> Capabilities {
            Capabilities::default()
        }
    }

